package chapter_2;
// 소스 2-18 향상된 for문의 예
public class source_2_18 {
    public static void main(String[] args) {
        int[] nArray = {32, 87, 3, 589, 12, 1076, 2000, 8, 622, 127};

        for (int e : nArray) {
            System.out.println(e + " ");
        }
        System.out.println();
    }
}
